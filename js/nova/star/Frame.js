var Frame = function () {
	var self = this;
	
	this.x = 0;
	this.y = 0;
	this.width = 0;
	this.height = 0;
	
	this.Frame = function (x, y, width, height) {
		self.this(x, y, width, height);
	};
	
	this.destroy = function () {
	};
	
	this.this = function (x, y, width, height) {
		x = typeof x == 'undefined' ? 0 : x;
		y = typeof y == 'undefined' ? 0 : y;
		width = typeof width == 'undefined' ? 800 : width;
		height = typeof height == 'undefined' ? 600 : height;
		self;
		self;
		self;
		self;
	};
	
	this.mutator_x = function (value) {
		self.mutator_x(value);
		return value;
	};
	
	this.mutator_y = function (value) {
		self.mutator_y(value);
		return value;
	};
	
	this.mutator_width = function (value) {
		self.mutator_width(value);
		return value;
	};
	
	this.mutator_height = function (value) {
		self.mutator_height(value);
		return value;
	};
	
	this.super = function () {
	};
	
	
};


